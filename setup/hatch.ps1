#!/usr/bin/env pwsh
<#
.SYNOPSIS
    SE OS spin-up wizard for Windows/PowerShell.
.DESCRIPTION
    Provisions Azure resources and configures GitHub Secrets for a new SE OS instance.
    Reads from setup.config.json in the repo root.
.EXAMPLE
    .\setup\hatch.ps1
    .\setup\hatch.ps1 -DryRun
#>
param(
    [switch]$DryRun,
    [string]$ConfigPath = "$PSScriptRoot\..\setup.config.json"
)

$ErrorActionPreference = "Stop"

# в”Җв”Җ Banner в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
Write-Host ""
Write-Host "  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—      в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—" -ForegroundColor Cyan
Write-Host "  в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв–Ҳв–Ҳв•”в•җв•җв•җв•җв•қ     в–Ҳв–Ҳв•”в•җв•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қ" -ForegroundColor Cyan
Write-Host "  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—       в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—" -ForegroundColor Cyan
Write-Host "  в•ҡв•җв•җв•җв•җв–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•қ       в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘в•ҡв•җв•җв•җв•җв–Ҳв–Ҳв•‘" -ForegroundColor Cyan
Write-Host "  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—     в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘" -ForegroundColor Cyan
Write-Host "  в•ҡв•җв•җв•җв•җв•җв•җв•қв•ҡв•җв•җв•җв•җв•җв•җв•қ      в•ҡв•җв•җв•җв•җв•җв•қ в•ҡв•җв•җв•җв•җв•җв•җв•қ" -ForegroundColor Cyan
Write-Host "  SE Operating System вҖ” Spin-Up Wizard" -ForegroundColor White
Write-Host ""

if ($DryRun) { Write-Host "  [DRY RUN вҖ” no Azure resources will be created]" -ForegroundColor Yellow; Write-Host "" }

# в”Җв”Җ Load config в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if (-not (Test-Path $ConfigPath)) {
    Write-Host "вң— setup.config.json not found." -ForegroundColor Red
    Write-Host "  Copy setup/config.template.json to setup.config.json and fill in your details."
    exit 1
}

$config = Get-Content $ConfigPath -Raw | ConvertFrom-Json
$alias  = $config.se_alias
$rg     = $config.deployment.resource_group
$loc    = $config.deployment.location
$swa    = $config.deployment.static_web_app_name
$fn     = $config.deployment.function_app_name
$sa     = $config.deployment.storage_account

Write-Host "  SE:            $($config.se_name) ($alias)" -ForegroundColor White
Write-Host "  Practice:      $($config.practice_area)" -ForegroundColor White
Write-Host "  Resource group: $rg ($loc)" -ForegroundColor White
Write-Host "  Static Web App: $swa" -ForegroundColor White
Write-Host ""

# в”Җв”Җ Check dependencies в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
foreach ($cmd in @("az","gh")) {
    if (-not (Get-Command $cmd -ErrorAction SilentlyContinue)) {
        Write-Host "вң— '$cmd' not found. Please install it first." -ForegroundColor Red
        exit 1
    }
}
Write-Host "вң“ Azure CLI and GitHub CLI found" -ForegroundColor Green

# в”Җв”Җ Resource group в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
Write-Host "`nStep 1/4 вҖ” Resource group..." -ForegroundColor Cyan
if (-not $DryRun) {
    az group create --name $rg --location $loc --output none 2>&1 | Out-Null
}
Write-Host "вң“ Resource group: $rg" -ForegroundColor Green

# в”Җв”Җ Storage account в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
Write-Host "`nStep 2/4 вҖ” Storage account..." -ForegroundColor Cyan
if (-not $DryRun) {
    az storage account create --name $sa --resource-group $rg --location $loc `
        --sku Standard_LRS --allow-blob-public-access false --output none 2>&1 | Out-Null
}
Write-Host "вң“ Storage: $sa" -ForegroundColor Green

# в”Җв”Җ Static Web App в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
Write-Host "`nStep 3/4 вҖ” Azure Static Web App..." -ForegroundColor Cyan
if (-not $DryRun) {
    $swaResult = az staticwebapp create --name $swa --resource-group $rg `
        --source "https://github.com/$env:GITHUB_REPOSITORY" --branch main `
        --app-location "web_ui" --output json 2>&1 | ConvertFrom-Json

    $deployToken = az staticwebapp secrets list --name $swa --resource-group $rg `
        --query "properties.apiKey" -o tsv 2>&1

    # Set GitHub Secret
    gh secret set AZURE_STATIC_WEB_APPS_API_TOKEN --body $deployToken
    Write-Host "вң“ Static Web App: $swa" -ForegroundColor Green
    Write-Host "вң“ GitHub Secret AZURE_STATIC_WEB_APPS_API_TOKEN set" -ForegroundColor Green
} else {
    Write-Host "  [DRY RUN] Would create: $swa" -ForegroundColor Yellow
}

# в”Җв”Җ Summary в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
Write-Host "`nв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ" -ForegroundColor Cyan
Write-Host "  SE OS setup complete!" -ForegroundColor Green
Write-Host ""
Write-Host "  Next: push to main to trigger deployment"
Write-Host "  git add -A && git commit -m 'feat: initial SE OS setup for $alias' && git push"
Write-Host ""
if (-not $DryRun) {
    Write-Host "  Your Command Center will be at:" -ForegroundColor White
    Write-Host "  https://$swa.azurestaticapps.net" -ForegroundColor Cyan
}
Write-Host "в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ" -ForegroundColor Cyan

# -- Register with the SE-OS network (central fleet visibility) -----------------
if (-not $DryRun) {
    try { & "$PSScriptRoot/register.ps1" -ConfigPath $ConfigPath } catch { Write-Host "  (register) skipped: $_" -ForegroundColor DarkGray }
}
